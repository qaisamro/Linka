const pool = require('../db/pool');

// ─── Intent Detection ────────────────────────────────────────────
const detectIntent = (message) => {
  const msg = message.trim().toLowerCase();

  if (/مرحب|أهلا|هلو|هلا|صباح|مساء|كيف حالك/.test(msg)) return 'greet';
  if (/كيف.*(أشارك|انضم|اسجل|اشترك)/.test(msg)) return 'how_to_join';
  if (/(الفعالي|النشاط|الأنشطة|فعاليات|ايش في|وش في)/.test(msg)) return 'find_events';
  if (/(ساعة|ساعات|تطوع|توثيق)/.test(msg)) return 'volunteer_hours';
  if (/(شارة|شارات|نقاط|انجاز|مكافأة)/.test(msg)) return 'badges';
  if (/(حي|منطقة|مكان|وين|أين|فين)/.test(msg)) return 'location';
  if (/(تسجيل.*دخول|دخول|لوجن)/.test(msg)) return 'login_help';
  if (/(بلدي|بلدية|الخليل|hebron)/.test(msg)) return 'about';
  if (/(شكر|ممتاز|رائع|عظيم|تمام|مشكور)/.test(msg)) return 'thanks';

  return 'default';
};

// ─── Main Chat Handler ───────────────────────────────────────────
const handleChat = async (message, userId = null) => {
  const intent = detectIntent(message);
  let reply = '';

  switch (intent) {
    case 'greet':
      reply = `أهلاً وسهلاً! أنا مساعد منصة شباب الخليل 👋\n\nيمكنني مساعدتك في:\n• 📅 عرض الفعاليات القادمة\n• ✅ كيفية الانضمام للفعاليات\n• ⏰ معرفة ساعات تطوعك\n• 🏅 الشارات والنقاط\n\nما الذي تريد معرفته؟`;
      break;

    case 'how_to_join':
      reply = `للانضمام لأي فعالية، اتبع هذه الخطوات:\n\n1️⃣ سجّل حسابك أو ادخل لحسابك الموجود\n2️⃣ تصفح الفعاليات من قائمة "الفعاليات" أو الخريطة\n3️⃣ اضغط على الفعالية التي تعجبك\n4️⃣ اضغط زر "انضم الآن"\n5️⃣ ستظهر الفعالية في ملفك الشخصي ✅\n\nبعد الحضور، سيؤكد المشرف حضورك وتحصل على النقاط والساعات تلقائياً!`;
      break;

    case 'find_events':
      try {
        const [rows] = await pool.query(
          `SELECT title, date, location_name, type, duration_hours
           FROM events
           WHERE status = 'active' AND date >= NOW()
           ORDER BY date ASC
           LIMIT 4`
        );

        if (rows.length === 0) {
          reply = 'لا توجد فعاليات قادمة حالياً، تابع المنصة لأخبار الفعاليات الجديدة! 📢';
        } else {
          const eventList = rows.map((e, i) => {
            const date = new Date(e.date).toLocaleDateString('ar-PS', {
              weekday: 'long', month: 'long', day: 'numeric'
            });
            return `${i + 1}. ${e.title}\n   📍 ${e.location_name}\n   📅 ${date}\n   ⏱️ ${e.duration_hours} ساعة`;
          }).join('\n\n');

          reply = `🗓️ الفعاليات القادمة في الخليل:\n\n${eventList}\n\nاذهب لصفحة الفعاليات لرؤية التفاصيل كاملة والتسجيل!`;
        }
      } catch (err) {
        reply = 'تفضل بزيارة صفحة الفعاليات لرؤية أحدث الفعاليات المتاحة 📅';
      }
      break;

    case 'volunteer_hours':
      reply = `⏰ نظام ساعات التطوع:\n\n• ساعاتك تُحسب تلقائياً بعد تأكيد المشرف لحضورك\n• كل فعالية تضيف عدد ساعاتها لرصيدك\n• يمكنك رؤية إجمالي ساعاتك في ملفك الشخصي\n\n🏅 مكافآت الساعات:\n• 5 ساعات → شارة "المتطوع"\n• 20 ساعة → شارة "المحترف"`;
      break;

    case 'badges':
      reply = `🏅 نظام الشارات والنقاط:\n\n**الشارات المتاحة:**\n🌱 المبادر — أول مشاركة\n⭐ الناشط — 5 مشاركات\n🏆 البطل — 10 مشاركات\n🤝 المتطوع — 5 ساعات تطوع\n🎖️ المحترف — 20 ساعة تطوع\n👑 القائد — 100 نقطة\n\n💡 تحصل على 10 نقاط لكل فعالية تحضرها!`;
      break;

    case 'location':
      reply = `📍 يمكنك إيجاد الفعاليات حسب المنطقة بطريقتين:\n\n1. **الخريطة التفاعلية** — اضغط على أيقونة الخريطة ورى جميع الفعاليات على خريطة الخليل\n2. **الفلتر** — في صفحة الفعاليات، اختر الحي الذي تريده من قائمة الفلترة\n\nأحياء الخليل المشمولة: وسط المدينة، باب الزاوية، جبل جوهر، القصبة، وغيرها!`;
      break;

    case 'login_help':
      reply = `🔐 للدخول لحسابك:\n\n1. اضغط "تسجيل الدخول" في أعلى الصفحة\n2. أدخل بريدك الإلكتروني وكلمة المرور\n3. اضغط "دخول"\n\nليس لديك حساب؟ اضغط "إنشاء حساب" — مجاني وسريع! 🚀`;
      break;

    case 'about':
      reply = `🏛️ منصة شباب الخليل هي مبادرة من بلدية الخليل لتعزيز مشاركة الشباب في الأنشطة والفعاليات المجتمعية.\n\nهدفنا ربط شباب الخليل بالفرص التطوعية والثقافية والرياضية في مدينتهم، وبناء جيل فاعل ومنتمي! 💪`;
      break;

    case 'thanks':
      reply = `شكراً لك! 😊 يسعدنا خدمتك.\n\nإذا احتجت أي مساعدة أخرى، أنا هنا دائماً. ولا تنسَ التسجيل في أقرب فعالية! 🌟`;
      break;

    default:
      reply = `عذراً، لم أفهم سؤالك بشكل كامل 🤔\n\nيمكنك سؤالي عن:\n• "ما هي الفعاليات القادمة؟"\n• "كيف أشارك في فعالية؟"\n• "كيف تعمل الشارات؟"\n• "كيف أحسب ساعات التطوع؟"`;
  }

  return reply;
};

module.exports = { handleChat };
